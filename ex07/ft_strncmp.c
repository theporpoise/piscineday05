int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	int				dif;

	i = 0;
	dif = 0;
	while (s1[i] != '\0' && (i < (n - 1)))
	{
		dif = s1[i] - s2[i];
		if (s2[i] == '\0' || (dif != 0))
			return (dif);
		i++;
	}
	if (s1[i] != '\0')
		return (0);
	return (-(s2[i]));
}
