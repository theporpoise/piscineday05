#include <string.h>

char	*ft_strcpy(char *dest, char *src)
{
	int i;

	i = 0;
	while (dest[i])
	{
		if (!src[i])
		{
			dest[i] = '\0';
		}
		else
		dest[i] = src[i];
		i++;
	}
	
	return (dest);
}
